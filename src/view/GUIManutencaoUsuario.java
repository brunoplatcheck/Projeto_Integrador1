    public void confirmarAlteracao(){
        try {
            CadastroVO cVO = new CadastroVO();
            if(jtfNome.getText().isEmpty() ||
                    jtfEmail.getText().isEmpty() ||
                    jtfLogin.getText().isEmpty() || 
                    jtfPassword.getText().isEmpty() ||
                    jtfCPF.getText().isEmpty() ||
                    jtfEndereco.getText().isEmpty() || 
                    jtfCep.getText().isEmpty() ||
                    jtfTelefone.getText().isEmpty() ||
                    jtfDiscord.getText().isEmpty() ||
                    jtfSteam.getText().isEmpty() ||
                    jtfJogo1.getText().isEmpty() || 
                    jtfJogo2.getText().isEmpty() ||
                    jtfJogo3.getText().isEmpty() ||
                    jtfJogo4.getText().isEmpty() ||
                    jtfJogo5.getText().isEmpty()) {
                JOptionPane.showMessageDialog(
                        null,
                        "Erro! Por favor insira as informações corretamente!",
                        "Erro",
                        JOptionPane.ERROR_MESSAGE);
            }else{
                cVO.setIdCadastro(Integer.parseInt(jtfCodigo.getText() ) );
                cVO.setNome(jtfNome.getText() );
                cVO.setEmail(jtfEmail.getText() );
                cVO.setLogin(jtfLogin.getText() );
                cVO.setPassword(jtfPassword.getText()  );
                cVO.setCpf(jtfCPF.getText() );
                cVO.setEndereco(jtfEndereco.getText()  );
                cVO.setCep(jtfCep.getText()  );
                cVO.setTelefone(jtfTelefone.getText()  );
                cVO.setDiscord(jtfDiscord.getText() );
                cVO.setSteam(jtfSteam.getText()  );
                cVO.setJogo1(jtfJogo1.getText()  );
                cVO.setJogo2(jtfJogo2.getText()  );
                cVO.setJogo3(jtfJogo3.getText()  );
                cVO.setJogo4(jtfJogo4.getText() );
                cVO.setJogo5(jtfJogo5.getText()  );
                
                CadastrarServicos cs = ServicosFactory.getCadastrarServicos();
                cs.alterarUsuario(cVO);
                
                JOptionPane.showMessageDialog(
                        null,
                        "usuario alterado com sucesso!");
            }//fim do if else
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null,
                    "Erro!" + e.getMessage());
        }
    }//fim do método confirmarAlteracao
