    private void deletarUsuario(){
        try {
            int linha = jtUsuarios.getSelectedRow();
            
            if(linha == -1){
                JOptionPane.showMessageDialog(null,
                        "Por favor selecione uma linha!");
            }else{               
                int resposta = JOptionPane.showConfirmDialog(null,
                        "Deseja mesmo deletar o usuario selecionado?",
                        "Deletar usuario",
                        JOptionPane.YES_NO_OPTION);
                if(resposta == JOptionPane.YES_OPTION){                   
                    CadastrarServicos cs = ServicosFactory.getCadastrarServicos();
                    String codUsuario = (String) jtUsuarios.getValueAt(linha, 0);
                    cs.deletarUsuario(Long.parseLong(codUsuario));
                
                    //Mensagem para o usuário
                    JOptionPane.showMessageDialog(null,
                        "usuario excluido com sucesso!");   
                }else{                   
                    JOptionPane.showMessageDialog(null,
                            "usuario não excluido");
                }//fim if else              
            }//fim do if else
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Erro ao deletar usuario!GUIManutencaoUsuario.deletar" + e.getMessage());
        }
    }
